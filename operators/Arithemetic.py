#  1. Python Arithmetic Operators
#  Arithmetic operators are used to perform mathematical operations like addition, 
# subtraction, multiplication, etc.
a=10
b=2
print("Addition",a+b)
print("Subtraction",a-b)
print("Products",a*b)
print("division",a/b)
print("floor division",a//b)
print("modulo",a%b)
print("power",a**a)
print("power",b**b)